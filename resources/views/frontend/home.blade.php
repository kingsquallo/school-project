                    country_id: country_id,
                    _token: '9BfYYnvVuK3kDJyjaD8LxQBGWSg29MKC36m1ICUI'
                },
                success: function (data) {
                    generate_option_from_json(data, 'country_to_state');
                }
            });
        });
    });
